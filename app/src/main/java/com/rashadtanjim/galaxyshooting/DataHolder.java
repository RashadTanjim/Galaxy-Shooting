package com.rashadtanjim.galaxyshooting;

// this Handler class is created for passing any data using OOP style
public class DataHolder {

    public String name = "";

    public DataHolder() {
    }

    public DataHolder(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
